#import "CALayer+SMRotaryWheel.h"


//////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////
@implementation CALayer (SMRotaryExtension)

//////////////////////////////////////////////////////////////////////////////////////////
- (void)setContentsWithImage:(UIImage*)img {
    self.contents = (id)img.CGImage;
    self.frame = CGRectMake(0, 0, CGImageGetWidth(img.CGImage), CGImageGetHeight(img.CGImage));
}

//////////////////////////////////////////////////////////////////////////////////////////
+ (id)layerWithImage:(UIImage*)img {
    CALayer* layer = [CALayer layer];
    [layer setContentsWithImage:img];
    return layer;
}

@end

